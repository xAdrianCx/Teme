# Rezolvare:

# A string type variable.
var_str = "Python"
# An int type variable.
var_int = 7
# A float type variable.
var_float = 7.1
# A bool type variable.
var_bool = True

# Verificam tipul fiecarei variabile.
print("Variabila 'var_str' este de tipul: " + str(type(var_str)))
print("Variabila 'var_int' este de tipul: " + str(type(var_int)))
print("Variabila 'var_float' este de tipul: " + str(type(var_float)))
print("Variabila 'var_bool' este de tipul: " + str(type(var_bool)))
